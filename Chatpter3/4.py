# capacitance
epsilon = 8.854187817e-12  # Permittivity of free space in F/m
area = 0.01  # Example area of the plates in square meters
separation_distance = 0.001  # Example separation distance in meters

# Calculate the capacitance
capacitance = epsilon * area / separation_distance

# Display the capacitance
print("Capacitance (C):", capacitance, "Farads")
